    private void showCarsActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_showCarsActionPerformed
        try {
            File fileCar=new File("./Cars.dat");
            CarsFile carFile=new CarsFile(fileCar);
            showCars shCar=new showCars();
            shCar.setVisible(true);
            //cantidad de elementos de ArrayList
            int arraySize=carFile.getAllCars().size();
            ArrayList<Cars> carsArray=carFile.getAllCars();
            String text="";
            //recorrido del array
            //
            for(int i=0; i<arraySize;i++){
                text="Nombre: "+carsArray.get(i).getName()+
                        "\nAÃ±o: "+carsArray.get(i).getYear()+
                        "\nKilometraje: "+carsArray.get(i).getMileage()+
                        "\nAmericano: "+carsArray.get(i).isAmerican()+
                        "\nSerie: "+carsArray.get(i).getSerie()+"\n";
                shCar.jTextArea1.setText(shCar.jTextArea1.getText()+"\n"+text);
            }
            shCar.jTextArea1.setEditable(false);
            
        } catch (IOException ex) {
            Logger.getLogger(MDI.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_showCarsActionPerformed

